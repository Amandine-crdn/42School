#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t				i;
	int					neg;
	unsigned long long	res;

	neg = 1;
	i = 0;
	res = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - 48);
		i++;
	}
	if (res > 9223372036854775807 && neg == 1)
		return (-1);
	else if (res > 9223372036854775807 && neg == -1)
		return (0);
	return (res * neg);
}
